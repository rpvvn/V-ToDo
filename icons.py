    @staticmethod
    def create_icon_from_svg(svg_data, size=32, color=None):
        """从SVG数据创建QIcon"""
        # 如果指定了颜色，替换currentColor
        if color:
            svg_data = svg_data.replace("currentColor", color)

        # 创建SVG渲染器
        svg_bytes = QByteArray(svg_data.encode("utf-8"))
        renderer = QSvgRenderer(svg_bytes)

        # 创建pixmap
        pixmap = QPixmap(size, size)
        pixmap.fill(Qt.transparent)

        # 渲染SVG到pixmap
        painter = QPainter(pixmap)
        renderer.render(painter)
        painter.end()

        return QIcon(pixmap)

    @staticmethod
    def get_app_icon(size=32):
        """获取应用程序主图标"""
        return IconManager.create_icon_from_svg(IconManager.APP_ICON_SVG, size)

